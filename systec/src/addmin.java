        // TODO add your handling code here:
        guardarDatos();
        if(nom.equals("")||cel.equals("")||marca.equals("")||modelo.equals("")||sn.equals("")){
            JOptionPane.showMessageDialog(null,"Complete los campos obligatorios porfavor:)");
        }else{
            /*System.out.println("Checando... \nNombre Cliente: "+nom+"\nTelefono: "+cel+"\nEquipo: "+equipo+"\nMarca: "+marca+
            "\nModelo: "+modelo+"\nSN: "+sn+"\nEliminador: "+eliminador+"\nContraseña: "+contraseña
            +"\nNotas: "+notas+"\nEmpleado: "+emp+"\nFecha y hora: "+time.format(LocalDateTime.now()));*/
            String sql="INSERT INTO sist (NOMBRE,TELEFONO,EQUIPO,MARCA,MODELO,SN,ELIMINADOR,CONTRASEÑA,NOTAS,EMPLEADO,FECHA) VALUES (?,?,?,?,?,?,?,?,?,?,?)";
            try {
                PreparedStatement ps= reg.prepareStatement(sql);
                ps.setString(1, nom);
                ps.setString(2, cel);
                ps.setString(3, equip);
                ps.setString(4, marca);
                ps.setString(5, modelo);
                ps.setString(6, sn);
                ps.setString(7, eliminador);
                ps.setString(8, contraseña);
                ps.setString(9, notas);
                ps.setString(10, emp);
                ps.setString(11, time.format(LocalDateTime.now()));
                ps.executeUpdate();                
                //System.out.println(nom+"\n"+cel+"\n"+equip+"\n"+marca+"\n"+modelo+"\n"+sn+"\n"+eliminador+"\n"+notas+"\n"+contraseña);
                JOptionPane.showMessageDialog(null,"Guardado correctamente:)");
            }catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Ha ocurrido un error:(\n"+ex);
                Logger.getLogger(addmin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
