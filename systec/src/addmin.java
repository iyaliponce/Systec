    public void guardarDatos(){
        nom = jTextField1.getText().toUpperCase();
        cel = jTextField2.getText().toUpperCase();
        equipo = jComboBox1.getSelectedItem().toString().toUpperCase();
        if(equipo.equals("Otro")){
            equip = JOptionPane.showInputDialog(null,"Ingrese el tipo de equipo:").toUpperCase();
        }else{
            equip = jComboBox1.getSelectedItem().toString().toUpperCase();
        }
        marca = jTextField3.getText().toUpperCase();
        modelo = jTextField4.getText().toUpperCase();
        if(jRadioButton1.isSelected()){
            eliminador = "SI";
        }else if(jRadioButton2.isSelected()){
            eliminador = "NO";
        }
        sn = jTextField5.getText().toUpperCase();
        notas = jTextArea1.getText().toUpperCase();
        time = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm");
        if(nom.isEmpty()||cel.isEmpty()||marca.isEmpty()||modelo.isEmpty()||sn.isEmpty()){
            //JOptionPane.showMessageDialog(null,"Complete los campos obligatorios porfavor:)");
        }else{
            emp=JOptionPane.showInputDialog(null,"¿Quien esta dando de alta el equipo?").toUpperCase();
            if(equip.equals("LAPTOP")||equip.equals("CPU")||equip.equals("ALL IN ONE")||equip.equals("TABLET")||equip.equals("CELULAR")||equip.equals("OTRO")){
                op=JOptionPane.showConfirmDialog(null,"¿Tiene contraseña el equipo?","",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
                //emp=JOptionPane.showInputDialog(null,"¿Quien esta dando de alta el equipo?").toUpperCase();
                if(op==0){
                    contraseña=JOptionPane.showInputDialog(null,"Ingrese la contraseña del equipo: ");
                }else{
                    contraseña="NO TIENE";
                }
            }else{
                contraseña="NO APLICA";
            }
        }
    }
