    void verTabla(String nt,String op){
        DefaultTableModel miModelo=new DefaultTableModel();
        //Titulo de las columnas
        miModelo.addColumn("FOLIO");
        miModelo.addColumn("NOMBRE");
        miModelo.addColumn("TELEFONO");
        miModelo.addColumn("EQUIPO");
        miModelo.addColumn("MARCA");
        miModelo.addColumn("MODELO");
        miModelo.addColumn("SN");
        miModelo.addColumn("ELIMINADOR");
        miModelo.addColumn("CONTRASEÃ‘A");
        miModelo.addColumn("NOTAS");
        miModelo.addColumn("EMPLEADO");
        miModelo.addColumn("FECHA");
        jTable1.setModel(miModelo);
        //Arreglo para llenar la tabla
        String []datos=new String[12];
        String sql="";
        if(nt.equals("")||op.equals("")||op.equals("Todos los datos")){
            sql="SELECT * FROM sist";
        }else{
            sql="SELECT * FROM sist WHERE "+op+" like '"+nt+"%'";
        }
        try {
            Statement sentencia=reg.createStatement();
            ResultSet rs= sentencia.executeQuery(sql);
            while (rs.next()) {
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
                datos[5]=rs.getString(6);
                datos[6]=rs.getString(7);
                datos[7]=rs.getString(8);
                datos[8]=rs.getString(9);
                datos[9]=rs.getString(10);
                datos[10]=rs.getString(11);
                datos[11]=rs.getString(12);
                miModelo.addRow(datos);
            }
            jTable1.setModel(miModelo);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Ha ocurrido un error:(\n"+ex);
            Logger.getLogger(buscadmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
